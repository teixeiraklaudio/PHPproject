    <h2 >O NATAL VIKING (YULE)</h2>

            <p>  Para afastar a escuridão e celebrar o retorno do Sol, os vikings reverenciavam árvores. Durante o ritual, uma fogueira era acesa para garantir calor no decorrer da noite mais longa do ano.

             Além disso, outras árvores eram montadas dentro das casas e decoradas com pedaços de comidas, estátuas e fitas. Ainda hoje, árvores são erguidas na época do Natal, sendo uma das heranças dos antigos europeus pagãos. 

            No entanto, os rituais de Yule eram muito mais controversos e assustadores, pois incluíam, ainda, o sacrifício de animais e estima-se que até mesmo de humanos. Embora não existam provas contundentes de pessoas que foram mortas durante o festival, há relatos que jovens foram assassinados para expiar os crimes mundanos.

            Acredita-se que o Yule tenha surgido com o sacrifício de um javali, que foi dado em oferta ao deus da virilidade, Freyr, e a sua irmã gêmea Freyja, a deusa da fertilidade. É possível encontrar tal menção no poema inglês Beowulf, onde o herói jurou ter matado o dragão Grendel durante uma cerimônia. Sabe-se que durante as celebrações jovens se vestiam com exuberantes fantasias e dançavam e cantavam em torno das casas em troca de bebidas e alimentos. </p>
            
            
