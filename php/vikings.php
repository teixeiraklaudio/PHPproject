    <p>Os vikings eram povos que habitaram o norte da Europa, na região chamada de Escandinávia, durante um período que abrange do século VIII até o século XI.</p>

      <h2>O que eram os Vikings?</h2>

    <p>A origem desse termo é alvo de intensa discussão entre os historiadores e, por isso, não há um consenso sobre a origem específica dessa palavra. Pórem, resumidamente os vikings ficaram conhecidos por serem um povo barbaro e saqueadores que faziam suas invazoes majoritariamnete de forma maritma.</p>

     <img id="noel" src="img/navio.jpg" alt="Embarcação viking"/>

   <p>Os vikings tambem são conhecidos por serem politeistas(acreditam em vários deuses) e por seus rituais incluindo sacrifícios até mesmo humanos. Atualmente a cultura viking antiga não é muito comum pórem quase um milhão dos britânicos vivos hoje são descendentes de Vikings, o que significa que um em cada 33 homens tendo assim dado continuidade a descendencia viking.</p>

